/**
 * Enum representing various actions in a backgammon game.
 */
public enum TypeAction
{
    EXIT,        // Action to exit the game
    PIP,         // Action to show current player's pip count
    DIENUM,      // Action to roll the dice
    THROW,       // Action to throw the dice
    MOVE,        // Action to move a piece
    HINT,        // Action to get a hint for the best move
    SKIP,        // Action to skip a turn
    DISPLAYMOVES, // Action to display available moves
    START,       // Action to start the game
    YIELD        // Action to yield or end the turn
}
